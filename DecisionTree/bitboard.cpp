int positionValue[2][8][256];


struct _InitPositionValue
{
	_InitPositionValue()
	{
		for (int i = 0; i < 8; i++)
		{
			for (int j = 0; j < 10; j++)
			{
				for (int k = 0; k < 9; k++)
				{
					_positionValueWrapper[1][i][j][k] = _positionValueWrapper[0][i][9 - j][k];
				}
			}
		}
		std::memset(positionValue, 0, sizeof(positionValue)); 
		for (int j = 0; j < 256; j++)
		{
			int x = j % 16 - 3;
			int y = j / 16 - 3;
			if (x < 0 || x >= 9 || y < 0 || y >= 10)
			{
				continue;
			}
			for (int country = 0; country < 2; country++)
			{
				for (int type = 0; type < 8; type++)
				{
					positionValue[country][type][j] = _positionValueWrapper[country][type][y][x];
				}
			}
		}
#ifdef MYDEBUG_POSITION_VALUE 
		for (int country = 0; country < 2; country++)
		{
			for (int type = 0; type < 8; type++)
			{
				std::cout << "country " << country << ", type " << type << '\n';
				for (int j = 0; j < 256; j++)
				{
					int x = j % 16 - 3;
					int y = j / 16 - 3;
					if (x == -1)
					{
						std::cout << '\n';
					}
					if (x < 0 || x >= 9 || y < 0 || y >= 10)
					{
						continue;
					}
					std::cout << positionValue[country][type][j] << ' ';
				}
				std::cout << '\n';
			} 
		}
#endif // MYDEBUG_POSITION_VALUE 
	}
}_initPositionValue;




