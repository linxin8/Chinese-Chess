struct _InitChessPositionHashTable
{
	_InitChessPositionHashTable()
	{ 
		std::memset(chessHashTable, 0, sizeof(chessHashTable));
		for (int j = 0; j < 256; j++)
		{
			int x = j % 16 - 3;
			int y = j / 16 - 3;
			if (x < 0 || x >= 9 || y < 0 || y >= 10)
			{
				continue;
			}
			for (int country = 0; country < 2; country++)
			{
				for (int type = 0; type < 8; type++)
				{
					if (type == 0)
					{
						chessHashTable[country][type][j] = 0;
					}
					else
					{ 
						chessHashTable[country][type][j] = chessPositionHashData[country][type - 1][y][x];
					}
				}
			}
		} 
	}
}_initChessPositionHashTable;
